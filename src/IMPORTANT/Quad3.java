package IMPORTANT;

public class Quad3 {

	public static void main(String[] args) 
	{
		int a,b;
		for(a=1; a<=1; a++)
		{
			for(b=1; b<=3; b++)
			{
				System.out.println("  ");
			}
		}
		
		for(a=1; a<=3; a++)
		{
			for(b=1; b<=3; b++)
			{
				System.out.print(" * ");
			}
			System.out.println();
		}
	}

}
